def getBraileChars(braile):
    braileLines = braile.split("\n\n")

    braileChars = []

    for braileLine in braileLines:
        braileRows = []
        lines = braileLine.split("\n")
        for line in lines:
            oneThirds = line.split(" ")
            braileRows.append(oneThirds)

        for c in range(len(braileRows[0])):
            char = ""
            for r in range(3):
                char+= braileRows[r][c]

            if (len(char)>0):
                braileChars.append(char)

    return braileChars


